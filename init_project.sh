#!/bin/bash
# =============================================================================
# Initialize a New RNA-seq Project
# =============================================================================
# Creates a project directory with the required structure and configuration
# files. The pipeline code itself stays in the cloned repository — this
# script sets up a SEPARATE data/analysis directory.
#
# Usage:
#   bash init_project.sh <project_dir> [genome]
#
# Examples:
#   bash init_project.sh /data/projects/2026_AML_RNAseq hg38
#   bash init_project.sh ~/analysis/Mouse_HSC mm10
#
# The created project directory will contain:
#   config.sh        — project-specific configuration (editable)
#   samples.tsv      — sample sheet template
#   comparisons.tsv  — comparison pairs template (optional)
#   fastq/           — place FASTQ files here
#   Gene_set/        — custom gene sets (copied from pipeline)
# =============================================================================
set -euo pipefail

PIPELINE_DIR="$(cd "$(dirname "$0")" && pwd)"
source "${PIPELINE_DIR}/scripts/utils.sh"

# ---------------------------------------------------------------------------
# Arguments
# ---------------------------------------------------------------------------
if [[ $# -lt 1 ]]; then
    echo "Usage: bash init_project.sh <project_dir> [genome]"
    echo ""
    echo "  project_dir  Path to the new project directory"
    echo "  genome       Reference genome: hg38 (default) or mm10"
    exit 1
fi

PROJECT_NEW="$(realpath -m "$1")"
GENOME="${2:-hg38}"

if [[ "${GENOME}" != "hg38" && "${GENOME}" != "mm10" ]]; then
    log_error "Unsupported genome: ${GENOME}. Use 'hg38' or 'mm10'."
    exit 1
fi

if [[ -d "${PROJECT_NEW}" ]]; then
    log_error "Directory already exists: ${PROJECT_NEW}"
    log_error "Remove it first or choose a different path."
    exit 1
fi

# ---------------------------------------------------------------------------
# Create directory structure
# ---------------------------------------------------------------------------
log_info "Creating project directory: ${PROJECT_NEW}"
mkdir -p "${PROJECT_NEW}"/{fastq,Gene_set}

# ---------------------------------------------------------------------------
# Generate config.sh (project-specific)
# ---------------------------------------------------------------------------
cat > "${PROJECT_NEW}/config.sh" <<CONFIGEOF
#!/bin/bash
# =============================================================================
# RNA-seq Pipeline Configuration — Project-specific
# =============================================================================
# Generated by init_project.sh on $(date '+%Y-%m-%d %H:%M:%S')
# Pipeline version: $(cat "${PIPELINE_DIR}/VERSION" | tr -d '[:space:]')
# =============================================================================

# --- Pipeline location ---
PIPELINE_DIR="${PIPELINE_DIR}"

# --- Project ---
PROJECT_DIR="${PROJECT_NEW}"
SAMPLES_TSV="\${PROJECT_DIR}/samples.tsv"
PLOT_TARGETS_FILE="\${PROJECT_DIR}/plot_targets.tsv"

# --- Reference Genome ---
GENOME="${GENOME}"

# STAR genome index directory
STAR_INDEX_HG38="\${HOME}/STAR2.7.3/hg38_sjdb149_RefSeq"
STAR_INDEX_MM10="\${HOME}/STAR2.7.3/mm10_sjdb149_RefSeq_ver241129"

# GTF annotation file
GTF_HG38="\${HOME}/Genome/hg38/hg38.ncbiRefSeq.gtf"
GTF_MM10="\${HOME}/Genome/mm10/mm10.ncbiRefSeq.gtf"

# --- STAR mapping parameters ---
STAR_THREADS=6
SJDB_OVERHANG=149
READ_MAP_NUMBER=-1

# --- featureCounts parameters ---
FEATURE_COUNTS_PAIRED=true
FEATURE_COUNTS_MULTI=true
GENE_ATTRIBUTE="gene_name"
EXON_ATTRIBUTE="exon_id"

# --- FASTQ settings ---
FASTQ_DIR="\${PROJECT_DIR}/fastq"

# --- Output directories ---
BAM_DIR="\${PROJECT_DIR}/BAM"
COUNTS_DIR="\${PROJECT_DIR}/counts"
DESEQ2_DIR="\${PROJECT_DIR}/DESeq2"
FGSEA_DIR="\${PROJECT_DIR}/fGSEA"
EXON_DE_DIR="\${PROJECT_DIR}/exon_DE"

# --- DESeq2 parameters ---
MIN_COUNT_FILTER=50
PCA_TOP_GENES=500
VOLCANO_PADJ=0.001
VOLCANO_LOG2FC=1

# --- fGSEA parameters ---
FGSEA_MIN_SIZE=15
FGSEA_MAX_SIZE=500
FGSEA_NPERM=10000
FGSEA_NPROC=12
FGSEA_PADJ=0.05

# --- Gene set files ---
CUSTOM_GMT="\${PROJECT_DIR}/Gene_set/HSC_set3.gmt"

# --- Single-sample GSEA / selected-gene visualization ---
SSGSEA_OUTPUT_DIR="\${PROJECT_DIR}/single_sample"
SSGSEA_MSIG_CATEGORIES="H,C2,C3,C4,C5,C6,C7"
SSGSEA_GMT_FILES="HSC_set3.gmt"
SSGSEA_MIN_SIZE=10
SSGSEA_MAX_SIZE=5000
SSGSEA_MIN_EXPR=1
SSGSEA_MIN_SAMPLES_FRACTION=0.5

# Groups for per-gene plots. Leave empty to auto-detect when exactly 2 groups exist.
GENE_PLOT_GROUPS=""
GENE_PLOT_PAIRED=true

# =============================================================================
# Derived values (do not edit below this line)
# =============================================================================
if [[ "\${GENOME}" == "hg38" ]]; then
    STAR_INDEX="\${STAR_INDEX_HG38}"
    GTF="\${GTF_HG38}"
    SPECIES="Homo sapiens"
    ORG_DB="org.Hs.eg.db"
elif [[ "\${GENOME}" == "mm10" ]]; then
    STAR_INDEX="\${STAR_INDEX_MM10}"
    GTF="\${GTF_MM10}"
    SPECIES="Mus musculus"
    ORG_DB="org.Mm.eg.db"
else
    echo "ERROR: Unsupported GENOME '\${GENOME}'." >&2
    exit 1
fi
CONFIGEOF

# ---------------------------------------------------------------------------
# Generate samples.tsv template
# ---------------------------------------------------------------------------
cat > "${PROJECT_NEW}/samples.tsv" <<'SAMPLESEOF'
sample_name	group	fq_prefix	lane_suffix
Sample1_pre	pre	Sample1_pre	L5
Sample1_post	post	Sample1_post	L5
Sample2_pre	pre	Sample2_pre	L5
Sample2_post	post	Sample2_post	L5
SAMPLESEOF

# ---------------------------------------------------------------------------
# Generate plot_targets.tsv template
# ---------------------------------------------------------------------------
cat > "${PROJECT_NEW}/plot_targets.tsv" <<'TARGETSEOF'
target_name	target_type	ssgsea	heatmap	gene_plot	volcano_highlight
