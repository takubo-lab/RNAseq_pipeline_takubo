TARGETSEOF

# ---------------------------------------------------------------------------
# Generate comparisons.tsv template (optional)
# ---------------------------------------------------------------------------
cat > "${PROJECT_NEW}/comparisons.tsv" <<'COMPEOF'
# Optional: specify comparison pairs explicitly.
# If this file is absent, all pairwise group comparisons are performed.
# Columns: group1 (numerator), group2 (denominator/reference)
group1	group2
post	pre
COMPEOF

# ---------------------------------------------------------------------------
# Copy gene set files
# ---------------------------------------------------------------------------
if [[ -d "${PIPELINE_DIR}/Gene_set" ]]; then
    cp -r "${PIPELINE_DIR}/Gene_set/"* "${PROJECT_NEW}/Gene_set/" 2>/dev/null || true
    log_info "Copied gene set files → ${PROJECT_NEW}/Gene_set/"
fi

if [[ -f "${PIPELINE_DIR}/plot_targets.tsv" ]]; then
    cp "${PIPELINE_DIR}/plot_targets.tsv" "${PROJECT_NEW}/plot_targets.tsv"
    log_info "Copied plot_targets.tsv → ${PROJECT_NEW}/"
fi

# ---------------------------------------------------------------------------
# Copy plot_config.default.yml for visualization customization
# ---------------------------------------------------------------------------
if [[ -f "${PIPELINE_DIR}/plot_config.default.yml" ]]; then
    cp "${PIPELINE_DIR}/plot_config.default.yml" "${PROJECT_NEW}/plot_config.default.yml"
    log_info "Copied plot_config.default.yml → ${PROJECT_NEW}/"
    log_info "個人カスタマイズ: cp plot_config.default.yml plot_config.yml"
fi

# ---------------------------------------------------------------------------
# Create .gitignore for project directory
# ---------------------------------------------------------------------------
cat > "${PROJECT_NEW}/.gitignore" <<'GITEOF'
# Large data files
fastq/
BAM/
*.bam
*.bai
*.fq.gz
*.fastq.gz

# R session
.Rhistory
.RData

# Pipeline lock
.pipeline.lock

# Personal plot config (use plot_config.default.yml as template)
plot_config.yml

# renv library (reproducible via renv.lock)
renv/library/
renv/staging/
renv/sandbox/

# Provenance (auto-generated)
# provenance.yml
GITEOF

# ---------------------------------------------------------------------------
# Initialize git repo in project (optional but recommended)
# ---------------------------------------------------------------------------
if command -v git &>/dev/null; then
    cd "${PROJECT_NEW}"
    git init -q
    git add -A
    git commit -q -m "Initialize RNA-seq project (pipeline v$(cat "${PIPELINE_DIR}/VERSION" | tr -d '[:space:]'))"
    log_info "Git repository initialized in ${PROJECT_NEW}"
fi

# ---------------------------------------------------------------------------
# Summary
# ---------------------------------------------------------------------------
echo ""
echo "======================================================"
echo "  Project initialized: ${PROJECT_NEW}"
echo "======================================================"
echo ""
echo "Next steps:"
echo "  1. Edit ${PROJECT_NEW}/config.sh"
echo "  2. Edit ${PROJECT_NEW}/samples.tsv"
echo "  3. Edit ${PROJECT_NEW}/plot_targets.tsv"
echo "  4. Place FASTQ files in ${PROJECT_NEW}/fastq/"
echo "  5. (Optional) Customize plots:"
echo "       cp ${PROJECT_NEW}/plot_config.default.yml ${PROJECT_NEW}/plot_config.yml"
echo "       vim ${PROJECT_NEW}/plot_config.yml"
echo "  6. Run the pipeline:"
echo ""
echo "     # Linux (full pipeline, conda環境内):"
echo "     conda activate rnaseq_takubo"
echo "     bash ${PIPELINE_DIR}/run_pipeline.sh --config ${PROJECT_NEW}/config.sh"
echo ""
echo "     # Windows/Mac (R解析のみ, RStudio推奨):"
echo "     cd ${PROJECT_NEW}"
echo "     Rscript ${PIPELINE_DIR}/scripts/04_deseq2.R config.sh"
echo ""
echo "======================================================"
